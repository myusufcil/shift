data class StringResource(
    val en: String,
    val tr: String,
    val es: String
) {
    fun get(language: Language): String {
        return when (language) {
            Language.ENGLISH -> en
            Language.TURKISH -> tr
            Language.SPANISH -> es
        }
    }
}

// Helper functions for month and day names
object LocalizationHelpers {
    fun getMonthName(month: Int, language: Language): String {
        return when (month) {
            1 -> StringResources.january.get(language)
            2 -> StringResources.february.get(language)
            3 -> StringResources.march.get(language)
            4 -> StringResources.april.get(language)
            5 -> StringResources.may.get(language)
            6 -> StringResources.june.get(language)
            7 -> StringResources.july.get(language)
            8 -> StringResources.august.get(language)
            9 -> StringResources.september.get(language)
            10 -> StringResources.october.get(language)
            11 -> StringResources.november.get(language)
            12 -> StringResources.december.get(language)
            else -> ""
        }
    }

    fun getDayNameShort(dayOfWeek: Int, language: Language): String {
        return when (dayOfWeek) {

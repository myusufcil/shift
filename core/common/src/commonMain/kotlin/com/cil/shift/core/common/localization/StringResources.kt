data class StringResource(
    val en: String,
    val tr: String,
    val es: String,
    val fr: String = en,
    val de: String = en,
    val pt: String = en,
    val ar: String = en,
    val ru: String = en,
    val hi: String = en,
    val ja: String = en,
    val zh: String = en
) {
    fun get(language: Language): String {
        return when (language) {
            Language.ENGLISH -> en
            Language.TURKISH -> tr
            Language.SPANISH -> es
            Language.FRENCH -> fr
            Language.GERMAN -> de
            Language.PORTUGUESE -> pt
            Language.ARABIC -> ar
            Language.RUSSIAN -> ru
            Language.HINDI -> hi
            Language.JAPANESE -> ja
            Language.CHINESE -> zh
        }
    }
}

// Helper functions for month and day names
object LocalizationHelpers {
    fun getMonthName(month: Int, language: Language): String {
        return when (month) {
            1 -> StringResources.january.get(language)
            2 -> StringResources.february.get(language)
            3 -> StringResources.march.get(language)
            4 -> StringResources.april.get(language)
            5 -> StringResources.may.get(language)
            6 -> StringResources.june.get(language)
